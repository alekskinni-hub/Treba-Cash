function getRandomSum(min, max) {
    min = Math.ceil(min) / 100;
    max = Math.floor(max) / 100;
    return (Math.floor(Math.random() * (max - min + 1)) + min) * 100;
}

function renderPushMessages(names, currency){
    arr = [];
    names.forEach(function(item) {
      elem = "<strong>" + item + "</strong> na inaprubahan para sa " + getRandomSum(1000, 10000) + " " + currency;
      arr.push(elem);
    });
    return arr;
}

